//Trovare il numero piu' grande
#include <stdio.h>

int main()
{
    unsigned int counter = 1, number, largest;
    
    printf("%s", "Enter the number: ");
    scanf("%u", &number);

    largest = number;

    while (counter < 10) {
        counter++;

        if (largest < number) {
            largest = number;
        }
        
        printf("%s", "Enter the number: ");
        scanf("%u", &number);
    }

    printf("Largest is: %u\n", largest);
    
}
